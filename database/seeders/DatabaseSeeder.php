<?php

namespace Database\Seeders;

use Illuminate\Database\Seeder;
use App\Models\User;

class DatabaseSeeder extends Seeder
{
    /**
     * 🌱 Основной сидер базы данных
     *
     * Здесь происходит первичное наполнение таблиц — пользователи, администраторы,
     * настройки, уведомления и т.д.
     */
    public function run(): void
    {
        /*
        |--------------------------------------------------------------------------
        | 👤 Пользователь "Test User"
        |--------------------------------------------------------------------------
        |
        | Используем updateOrCreate, чтобы избежать дубликатов при повторном запуске сидера.
        | Если пользователь с указанным email уже существует — он будет обновлён.
        |
        */
        User::updateOrCreate(
            ['email' => 'test@example.com'], // 🎯 Поиск по email
            [
                'name' => 'Test User',                     // 👤 Имя
                'email_verified_at' => now(),              // ✅ Email подтверждён
                'password' => bcrypt('123456'),            // 🔐 Простой пароль (хешируется)
            ]
        );

        /*
        |--------------------------------------------------------------------------
        | 👑 Пользователь "Admin"
        |--------------------------------------------------------------------------
        |
        | Создаём администратора — тоже через updateOrCreate.
        | Убедись, что в таблице users есть поле `is_admin`.
        |
        */
        User::updateOrCreate(
            ['email' => 'admin@example.com'],
            [
                'name' => 'Admin',
                'email_verified_at' => now(),
                'password' => bcrypt('password'),
                'is_admin' => true, // 🛡️ Признак администратора
            ]
        );

        /*
        |--------------------------------------------------------------------------
        | 📦 Запуск дополнительных сидеров
        |--------------------------------------------------------------------------
        |
        | Здесь можно подключить другие классы сидеров, которые наполняют таблицы:
        | - пользователи
        | - уведомления
        | - системные настройки
        |
        */
        $this->call([
            UsersTableSeeder::class,         // 👥 Дополнительные пользователи
        ]);
    }
}
